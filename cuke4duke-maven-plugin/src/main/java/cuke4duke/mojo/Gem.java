package cuke4duke.mojo;

public class Gem {

    private String name;
    private String version;

    void setName(String name) {
        this.name = name;
    }

    String getName() {
        return this.name;
    }

    void setVersion(String version) {
        this.version = version;
    }

    String getVersion() {
        return this.version;
    }
}
